function display_test_result {
	if [ $1 -eq 0 ]; then
		[ ${DISPLAY_ERROR_ONLY} -eq 0 ] && echo "[SUCCESS] ${TEST_NAME}"
	elif [ $1 -eq 1 ]; then
		echo "[FAILURE] ${TEST_NAME}"
	elif [ $1 -eq 2 ]; then
		echo '[ERROR] Problem with ${TEST_NAME}'
	fi

	return $1
}

function file_exists {
	TEST_NAME="File exist"
	if [ -z "${1}" ]; then
		TEST_RESULT="Missing file to check"
		return 2
	fi
	TEST_RESULT=""
	TEST_NAME="${TEST_NAME}: $1"
	[ $(ls -1 $1 2>/dev/null | wc -l) -gt 0 ] && return 0
	return 1
}

function file_exist_test {
	file_exists "$1"
	display_test_result $?
}

function check_process_exists {
	TEST_NAME="Process exist"
	if [ -z "${1}" ]; then
		TEST_RESULT="Missing process name to check"
		return 2
	fi
	TEST_RESULT=""
	TEST_NAME="${TEST_NAME}: $1"
	local PEXISTS=$(ps aux | grep "$1" | grep -v grep | wc -l)
	[ ${PEXISTS} -eq 0 ] && return 1
	return 0
}

function check_process_exist_test {
	check_process_exists "$1"
	display_test_result $?
}

function check_user_process_exists {
	TEST_NAME="Process (for a user) exist"
	if [ -z "${1}" ]; then
		TEST_RESULT="Missing username to check"
		return 2
	fi
	local PNMAME_TRUNCATED="$(echo $1 | cut -c1-7)"
	TEST_RESULT=""
	TEST_NAME="${TEST_NAME}: $1 (Truncated: ${PNMAME_TRUNCATED})"
	local PEXISTS=$(ps aux | grep "^${PNMAME_TRUNCATED}" | wc -l)
	[ ${PEXISTS} -eq 0 ] && return 1
	return 0
}

function check_user_process_exist_test {
	check_user_process_exists "$1"
	display_test_result $?
}

function check_package_exists {
	TEST_NAME="Package exist"
	if [ -z "${1}" ]; then
		TEST_RESULT="Missing package to check"
		return 2
	fi
	TEST_RESULT=""
	TEST_NAME="${TEST_NAME}: $1"
	dpkg -l "$1" 2>/dev/null >/dev/null
	return $?
}

function check_package_exist_test {
	check_package_exists "$1"
	display_test_result $?
}

